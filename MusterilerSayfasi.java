     public  static  void musteriListele(String sorgu){
        DefaultTableModel model;
        musteriler.clear();
        Statement state=null;
        ResultSet rs=null;
         model=(DefaultTableModel)tblTumMusteriler.getModel();
                 model.setRowCount(0);        
       try{
            state=dbBaglanti().createStatement();
            rs=state.executeQuery(sorgu);
            
            while(rs.next()){
                int m_id=rs.getInt("aMusteri_id");
                String m_adi=rs.getString("aMusteri_adi");
                String m_soyadi=rs.getString("aMusteri_soyadi");     
                int m_yas=rs.getInt("aMusteri_yas");
                String m_tc=rs.getString("aMusteri_tc");
                String  m_tel=rs.getString("aMusteri_tel");
                String m_meslek=rs.getString("aMusteri_meslek");
                
                musteriler.add(new Musteri(m_id,m_adi,m_soyadi,m_yas,m_meslek,m_tc,m_tel));
            }
                                      DbConnect.dbBaglanti().close();

             if(musteriler!=null){                    
                    for(Musteri musteri:musteriler){       
                        
                        Object[] add={musteri.getAd(),musteri.getSoyad(),musteri.getTc(),musteri.getYas(),musteri.getTel(),musteri.getMeslegi()};
                                            model.addRow(add);                  
                    }
             }
                                 tblTumMusteriler.setModel(model);             
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
      

        
    
    
    }
    
    
