    private void btnMusteriSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMusteriSilActionPerformed
        // TODO add your handling code here:
                   int i=tblTumMusteriler.getSelectedRow();

       if( JOptionPane.showConfirmDialog(null, musteriler.get(i).getAd()+ " "+musteriler.get(i).getSoyad()+" isimli Müşteriyi silmek istiyormusunuz?", "Uyarı" ,0)==JOptionPane.YES_OPTION){
           int id=musteriler.get(i).getM_id();
           
           Statement st=null;
           
           try{
               String sorgu="delete  from aktifMusteriler where aMusteri_id="+id+" ";
               st=dbBaglanti().createStatement();
               int sonuc=st.executeUpdate(sorgu);
                                         DbConnect.dbBaglanti().close();

               if(sonuc==1){
                   JOptionPane.showMessageDialog(null,"Müşteri Silindi");
                          sorgu="select * from aktifMusteriler";

                   musteriListele(sorgu);

               }
           }catch(Exception e){
               JOptionPane.showMessageDialog(null, e);
           }
           
           
       }
                
    }//GEN-LAST:event_btnMusteriSilActionPerformed

